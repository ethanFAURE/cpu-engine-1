{
}

void StateIdle::OnExecute(Ship& cur)
{
	// Blink every 3 seconds
	if ( cur.GetFSM()->totalTime>3.0f )
	{
		cur.GetFSM()->ToState(CPU_ID(StateBlink));
		return;
	}
}

void StateIdle::OnExit(Ship& cur, int to)
{
}

